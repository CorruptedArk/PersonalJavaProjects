    private void operations(String buttonText){
        if (digitsEntered.length() > 0) { 
                    doubleEntered = Double.parseDouble(digitsEntered.toString());
                    if (currentOperator == null) {
                        result = doubleEntered;
                    } else {
                        switch (currentOperator) {
                            case "+":
                                result = result + doubleEntered;
                                break;
                            case "-":
                                result = result - doubleEntered;
                                break;
                            case "x":
                                result = result * doubleEntered;
                                break;
                            case "/":
                                result = result / doubleEntered;
                                break;
                            case "=":
                                result = doubleEntered;
                                break;
                            case "sqrt": 
                                result = Math.sqrt(doubleEntered);
                                break;
                            case "power":
                                result = Math.pow(result, doubleEntered);
                                break;
                            case "cos":
                                result = Math.cos(Math.toRadians(doubleEntered));
                                break;
                            case "sin":
                                result = Math.sin(Math.toRadians(doubleEntered));
                                break;
                            case "tan":
                                result = Math.tan(Math.toRadians(doubleEntered));
                                break;
                            case "logbM":
                                result = (Math.log10(m)/Math.log10(b));
                            case "b=":
                                b = doubleEntered;
                                result = b;
                                break;
                            case "M=":
                                m = doubleEntered;
                                result = m;
                            default:
                                break;
                        }
                        textField1.setText(Double.toString(result));
                    }
                }
                digitsEntered.setLength(0); // clear out the input so a new integer can be entered
                currentOperator = buttonText; // remember the operator
    }
            
